operation = input("Please write + or - to pick calculator: ")

if operation == "+":
    number1 = int(input("Please enter nr 1: "))
    number2 = int(input("Please enter nr 2: "))
    print(number1 + number2)

if operation == "-":
    number1 = int(input("Please enter nr 1: "))
    number2 = int(input("Please enter nr 2: "))
    print(number1 - number2)
